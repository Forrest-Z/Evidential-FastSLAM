#ifndef EFS_LINE_H_
#define EFS_LINE_H_

#include <vector>

#include "efslam/utils/Point.h"


namespace efs {

template<int N>
class Line : public std::vector< Pointm<N> > {
public:
					Line( const Pointm<N> &start, const Pointm<N> &end );
	virtual 		~Line();

	virtual void	extend( const Pointm<N> &end );	// extend the line from old end to end

private:
	inline void		bresenham( const Pointm<N> &start, const Pointm<N> &end );
};


} /* namespace efs */


#include "Line.hpp"

#endif /* EFS_LINE_H_ */
